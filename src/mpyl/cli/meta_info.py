import os
from importlib.metadata import version as version_meta, distribution

VDB_LOGO = """
                                         .::.               
                                         :~~~^:.            
                                         ^~~~~~~^:.         
               .........................:~~~~~~~~~^^.       
             .^~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~^:.    
             .^~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~^:. 
               ^~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~:
                :~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~^:. 
                 :~~~~~~~~~~~~~~^~~~~~~~~~~~~~~~~~~~~^:.    
                  .^~~~~~~~~~~~~^........^~~~~~~~~^:.       
                   .^~~~~~~~~~~~~^       ^~~~~~^^.          
                     ^~~~~~~~~~~~~^.     :~~^^:.            
                      :~~~~~~~~~~~~^.     ::.               
                       :~~~~~~~~~~~~~:                      
                        .^~~~~~~~~~~~~:                     
      .::^^^^^^::.       .^~~~~~~~~~~~~^                    
   .:^~~~~~~~~~~~~^:.      ^~~~~~~~~~~~~^.                  
  :^~~~~~~~~~~~~~~~~^:::::::^~~~~~~~~~~~~^.                 
 :~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~.                
.^~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~:               
.~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~:              
 ^~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~^             
 .~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~^.            
  .^~~~~~~~~~~~~~~~~^:........................              
    .:^~~~~~~~~~~^^.                                        
       .:::^^:::.
"""


class MetaInfo:
    @staticmethod
    def print_version():
        print(f"Version {version_meta('mpyl')}")

    @staticmethod
    def print_about():
        dist = distribution('mpyl')
        print(os.linesep.join(str(dist.metadata).split(os.linesep)[1:16]))
        print(VDB_LOGO)
